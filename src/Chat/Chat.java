package Chat;

public class Chat {
	private int group_chat_id;
	private String group_room_name;
	private String group_member;
	private int group_member_number;
	private String log_chat;
	private String log_time;
	private String profile_photo;
	

	public int getGroup_chat_id() {
		return group_chat_id;
	}

	public void setGroup_chat_id(int group_chat_id) {
		this.group_chat_id = group_chat_id;
	}

	public String getGroup_room_name() {
		return group_room_name;
	}

	public void setGroup_room_name(String group_room_name) {
		this.group_room_name = group_room_name;
	}

	public String getGroup_member() {
		return group_member;
	}

	public void setGroup_member(String group_member) {
		this.group_member = group_member;
	}

	public int getGroup_member_number() {
		return group_member_number;
	}

	public void setGroup_member_number(int group_member_number) {
		this.group_member_number = group_member_number;
	}

	public String getLog_chat() {
		return log_chat;
	}

	public void setLog_chat(String log_chat) {
		this.log_chat = log_chat;
	}

	public String getLog_time() {
		return log_time;
	}

	public void setLog_time(String log_time) {
		this.log_time = log_time;
	}

	public String getProfile_photo() {
		return profile_photo;
	}

	public void setProfile_photo(String profile_photo) {
		this.profile_photo = profile_photo;
	}

}
