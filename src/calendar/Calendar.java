package calendar;

public class Calendar {
	int calendar_num;
	int member_num;
	String calendar_day;
	String calendar_memo;
	public int getCalendar_num() {
		return calendar_num;
	}
	public void setCalendar_num(int calendar_num) {
		this.calendar_num = calendar_num;
	}
	public int getMember_num() {
		return member_num;
	}
	public void setMember_num(int member_num) {
		this.member_num = member_num;
	}
	public String getCalendar_day() {
		return calendar_day;
	}
	public void setCalendar_day(String calendar_day) {
		this.calendar_day = calendar_day;
	}
	public String getCalendar_memo() {
		return calendar_memo;
	}
	public void setCalendar_memo(String calendar_memo) {
		this.calendar_memo = calendar_memo;
	}
	
}
