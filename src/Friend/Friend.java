package Friend;

public class Friend {
	private int friend_num;
	private int me;
	private int you;
	
	public int getFriend_num() {
		return friend_num;
	}
	public void setFriend_num(int friend_num) {
		this.friend_num = friend_num;
	}
	public int getMe() {
		return me;
	}
	public void setMe(int me) {
		this.me = me;
	}
	public int getYou() {
		return you;
	}
	public void setYou(int you) {
		this.you = you;
	}
	
	
	
	
}
