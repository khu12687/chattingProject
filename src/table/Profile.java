package table;

public class Profile {
	private int profile_num;
	private int member_num;
	private String profile_photo;
	private String profile_bg;
	private String profile_nickname;
	private String profile_status;
	
	public int getProfile_num() {
		return profile_num;
	}
	public void setProfile_num(int profile_num) {
		this.profile_num = profile_num;
	}
	public int getMember_num() {
		return member_num;
	}
	public void setMember_num(int member_num) {
		this.member_num = member_num;
	}
	public String getProfile_photo() {
		return profile_photo;
	}
	public void setProfile_photo(String profile_photo) {
		this.profile_photo = profile_photo;
	}
	public String getProfile_bg() {
		return profile_bg;
	}
	public void setProfile_bg(String profile_bg) {
		this.profile_bg = profile_bg;
	}
	public String getProfile_nickname() {
		return profile_nickname;
	}
	public void setProfile_nickname(String profile_nickname) {
		this.profile_nickname = profile_nickname;
	}
	public String getProfile_status() {
		return profile_status;
	}
	public void setProfile_status(String profile_status) {
		this.profile_status = profile_status;
	}
	
	
}
