package table;

public class MyLog {
	int log_num;
	int group_member_num ;
	String log_chat;
	
	public int getLog_num() {
		return log_num;
	}
	public void setLog_num(int log_num) {
		this.log_num = log_num;
	}
	public int getGroup_member_num() {
		return group_member_num;
	}
	public void setGroup_member_num(int group_member_num) {
		this.group_member_num = group_member_num;
	}
	public String getLog_chat() {
		return log_chat;
	}
	public void setLog_chat(String log_chat) {
		this.log_chat = log_chat;
	}
	
}
